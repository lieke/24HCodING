package nl.ing.cla.model;

public class Chore {
	int id;
	String name;	
	double price;
	String status;
	
	public void setId(int id) {
		this.id = id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setPrice(float price) {
		this.price = price;
	}
	public void setStatus(String status) {
		this.status = status;
	}	

}
